package com.rays.basic;

public class FindMaximumNo {

	public static void main(String[] args) {

		int n1 = 1;
		int n2 = 10;

		if (n1 > n2) {

			System.out.println("max no = " + n1);

		} else if (n1 == n2) {

			System.out.println("both no are equals");

		} else {
			System.out.println("max no = " + n2);
		}

	}

}
